void main() {

  // lops de repetição 

  for (int i = 0; i <= 9+1; i++) {
    print("fiz $i voltas no mundo");
  } 

  // outro laço
  for (int i = 0; i < 11; i++)
    print(i);



  int idade = 2;
// loop rodando dentro de condiçoes 
  if (idade == 20) {
//  loop infinito (cuidado)
    for (int ano = 0; ano >= 0; ano++)
      print(ano);
  } 

  else {
    print("naoo");
  }
}